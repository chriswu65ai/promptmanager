import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const migrationsDir = path.join(repoRoot, 'supabase', 'migrations');
const installerPath = path.join(repoRoot, 'supabase', 'installer', 'schema.sql');
const CHECK_MODE = process.argv.includes('--check');

function normalizeSql(sql) {
  return sql.trim().replace(/\s+$/gm, '').concat('\n');
}

async function buildInstallerSql() {
  const entries = await readdir(migrationsDir, { withFileTypes: true });
  const migrationFiles = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.sql'))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  if (!migrationFiles.length) {
    throw new Error('No SQL migration files found in supabase/migrations.');
  }

  const sections = await Promise.all(
    migrationFiles.map(async (file) => {
      const sql = await readFile(path.join(migrationsDir, file), 'utf8');
      return `-- >>> ${file}\n${normalizeSql(sql)}`;
    }),
  );

  return `-- Auto-generated by scripts/build-schema-installer.mjs\n-- Source of truth: supabase/migrations/*.sql\n\n${sections.join('\n')}`;
}

async function run() {
  const generatedSql = await buildInstallerSql();

  if (CHECK_MODE) {
    const existingSql = await readFile(installerPath, 'utf8').catch(() => null);
    if (existingSql !== generatedSql) {
      throw new Error('supabase/installer/schema.sql is out of sync with supabase/migrations. Run: npm run build:schema-installer');
    }
    console.log('Schema installer artifact is synchronized with supabase/migrations.');
    return;
  }

  await writeFile(installerPath, generatedSql, 'utf8');
  console.log(`Wrote ${path.relative(repoRoot, installerPath)} from ${path.relative(repoRoot, migrationsDir)}.`);
}

run().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
